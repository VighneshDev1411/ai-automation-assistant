const CATEGORIES = [
  { value: 'all', label: 'All Integrations' },
  { value: 'productivity', label: 'Productivity' },
  { value: 'communication', label: 'Communication' },
  { value: 'development', label: 'Development' },
  { value: 'finance', label: 'Finance' },
  { value: 'marketing', label: 'Marketing' },
]

interface IntegrationListProps {
  connectedIntegrations?: Array<{
    provider: string
    status: IntegrationStatus
    lastSynced?: string | null
  }>

  onConnect?: (provider: string) => void
  onDisconnect?: (provider: string) => void
  onSync?: (provider: string) => void
  onSettings?: (provider: string) => void
}

export function IntegrationList({
  connectedIntegrations = [],
  onConnect,
  onDisconnect,
  onSync,
  onSettings,
}: IntegrationListProps) {
  const [searchQuery, setSearchQuery] = useState('')
  const [selectedCategory, setSelectedCategory] = useState('all')

  const filteredIntegrations = INTEGRATIONS.filter(integration => {
    const matchesSearch =
      integration.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
      integration.description.toLowerCase().includes(searchQuery.toLowerCase())

    const matchesCategory =
      selectedCategory === 'all' || integration.category === selectedCategory

    return matchesSearch && matchesCategory
  })

  const getIntegrationStatus = (provider: string) => {
    const connected = connectedIntegrations.find(i => i.provider === provider)
    return (
      connected || { provider, status: 'disconnected' as IntegrationStatus }
    )
  }

  return (
    <div className="space-y-6">
      {/* Filters */}
      <div className="flex flex-col sm:flex-row gap-4">
        <div className="relative flex-1">
          <Search className="absolute left-3 top-1/2 -translate-y-1/2 h-4 w-4 text-muted-foreground" />
          <Input
            placeholder="Search integrations..."
            value={searchQuery}
            onChange={e => setSearchQuery(e.target.value)}
            className="pl-10"
          />
        </div>
        <div className="flex gap-2 flex-wrap">
          {CATEGORIES.map(category => (
            <Badge
              key={category.value}
              variant={
                selectedCategory === category.value ? 'default' : 'outline'
              }
              className="cursor-pointer"
              onClick={() => setSelectedCategory(category.value)}
            >
              {category.label}
            </Badge>
          ))}
        </div>
      </div>

      {/* Integration Grid */}
      <ResponsiveGrid cols={{ default: 1, lg: 2 }} gap={6}>
        {filteredIntegrations.map(integration => {
          const status = getIntegrationStatus(integration.provider)

          return (
            <IntegrationCard
              key={integration.provider}
              provider={integration.provider}
              name={integration.name}
              description={integration.description}
              icon={integration.icon}
              status={status.status}
              lastSynced={status.lastSynced}
              onConnect={() => onConnect?.(integration.provider)}
              onDisconnect={() => onDisconnect?.(integration.provider)}
              onSync={() => onSync?.(integration.provider)}
              onSettings={() => onSettings?.(integration.provider)}
            />
          )
        })}
      </ResponsiveGrid>
    </div>
  )
}
